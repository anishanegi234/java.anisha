// Simple Java program to demonstrate JDK, JRE, and JVM involvement
public class JDKJREJVMExample {
    public static void main(String[] args) {
        // JVM executes this code
        System.out.println("Understanding JDK, JRE, and JVM!");
        
        // Using JDK features: Compile-time constants
        final int compiledValue = 10; 
        System.out.println("Compiled value: " + compiledValue);
        
        // Demonstrating JRE features: Library usage
        System.out.println("Current time in milliseconds: " + System.currentTimeMillis());
    }
}
