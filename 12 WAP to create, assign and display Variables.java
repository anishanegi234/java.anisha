// Program to create, assign, and display variables in Java
public class VariablesDemo {
    public static void main(String[] args) {
        // Creating and assigning values to different types of variables

        // Integer variable
        int age = 25;

        // Double variable
        double price = 99.99;

        // Character variable
        char grade = 'A';

        // String variable
        String name = "kavita";

        // Boolean variable
        boolean isJavaFun = true;

        // Displaying the values of the variables
        System.out.println("Name: " + name);  // Displays the string variable
        System.out.println("Age: " + age);    // Displays the integer variable
        System.out.println("Price: $" + price); // Displays the double variable
        System.out.println("Grade: " + grade);  // Displays the character variable
        System.out.println("Is Java Fun? " + isJavaFun); // Displays the boolean variable
    }
}

